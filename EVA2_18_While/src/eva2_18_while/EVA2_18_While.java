public class EVA2_18_While {
    
    public static void main(String[] args) {
       int i = 1;
        while(i <= 10){ //Se va a repetir si la condicion es verdad
            System.out.println("Alan Armando Rodríguez Jiménez");
            
             i++;
       }
        //Simular un for con un do while
        i = 1;
    do{
        
        System.out.println("Alan Armando Rodriguez Jimenez");
        i++;  
    }while(i <= 10);
    }
    
}
